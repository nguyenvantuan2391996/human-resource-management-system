package hrm.entities;

public class cauhoi {
	private int macauhoi;
	private String chude;
	private String cauhoi;
	private String dapan;
	private String a;
	private String b;
	private String c;
	private String d;

	public cauhoi() {
		super();
	}

	public cauhoi(int macauhoi, String chude, String cauhoi, String dapan, String a, String b, String c, String d) {
		super();
		this.macauhoi = macauhoi;
		this.chude = chude;
		this.cauhoi = cauhoi;
		this.dapan = dapan;
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
	}

	public int getMacauhoi() {
		return macauhoi;
	}

	public void setMacauhoi(int macauhoi) {
		this.macauhoi = macauhoi;
	}

	public String getChude() {
		return chude;
	}

	public void setChude(String chude) {
		this.chude = chude;
	}

	public String getCauhoi() {
		return cauhoi;
	}

	public void setCauhoi(String cauhoi) {
		this.cauhoi = cauhoi;
	}

	public String getDapan() {
		return dapan;
	}

	public void setDapan(String dapan) {
		this.dapan = dapan;
	}

	public String getA() {
		return a;
	}

	public void setA(String a) {
		this.a = a;
	}

	public String getB() {
		return b;
	}

	public void setB(String b) {
		this.b = b;
	}

	public String getC() {
		return c;
	}

	public void setC(String c) {
		this.c = c;
	}

	public String getD() {
		return d;
	}

	public void setD(String d) {
		this.d = d;
	}

}
